"""
Base abstractions for LLM providers.

Defines the unified response structure (ProviderResponse) and the
LLMProvider protocol that all concrete providers must implement.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Protocol, runtime_checkable


@dataclass
class ToolCall:
    """A single tool call within a ProviderResponse."""

    tool: str
    arguments: dict[str, Any] = field(default_factory=dict)
    call_id: str | None = None


@dataclass
class ProviderResponse:
    """Unified response from any LLM provider.

    Attributes:
        content: Text content generated by the model.
        tool_calls: List of tool calls the model wants to make.
        finish_reason: Reason the model stopped generating (e.g. "stop", "tool_calls").
        total_tokens: Total tokens consumed for this request.
        raw: Optional raw API response (sanitised — must never contain API keys).
    """

    content: str
    tool_calls: list[ToolCall]
    finish_reason: str | None
    total_tokens: int
    raw: dict[str, Any] | None = None


